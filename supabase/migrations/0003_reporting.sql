select c.id, 'vi', case c.code
  when 'service' then 'Dịch vụ'
  when 'security' then 'An ninh trật tự'
  when 'environment' then 'Môi trường'
  when 'other' then 'Khác'
end
from report_categories c
on conflict do nothing;

insert into report_category_translations (category_id, locale, name)
select c.id, 'en', case c.code
  when 'service' then 'Service'
  when 'security' then 'Security & order'
  when 'environment' then 'Environment'
  when 'other' then 'Other'
end
from report_categories c
on conflict do nothing;
